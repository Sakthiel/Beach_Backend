CREATE TABLE SHOW
(
    ID       BIGINT GENERATED BY DEFAULT AS IDENTITY,
    MOVIE_ID VARCHAR(50)   NOT NULL,
    DATE     DATE          NOT NULL,
    SLOT_ID  INT REFERENCES SLOT (ID),
    SCREEN_ID INT REFERENCES SCREEN (ID),
    COST     NUMERIC(6, 2) NOT NULL,
    UNIQUE (DATE, SLOT_ID , SCREEN_ID),
    PRIMARY KEY (ID)
);